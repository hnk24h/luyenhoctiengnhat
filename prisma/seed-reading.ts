async function main() {
  console.log('🌱  Seeding reading passages...\n');

  let created = 0;
  let skipped = 0;

  for (const p of passages) {
    // Skip if exact title already exists
    const exists = await prisma.readingPassage.findFirst({
      where: { title: p.title },
      select: { id: true },
    });

    if (exists) {
      console.log(`  ⏭  Skip (exists): ${p.title}`);
      skipped++;
      continue;
    }

    await prisma.readingPassage.create({
      data: {
        title:     p.title,
        titleVi:   p.titleVi   ?? null,
        content:   p.content.trim(),
        summary:   p.summary   ?? null,
        level:     p.level,
        type:      p.type,
        source:    p.source    ?? null,
        sourceUrl: (p as any).sourceUrl ?? null,
        tags:      p.tags?.length ? p.tags : Prisma.JsonNull,
        published: true,
      },
    });

    console.log(`  ✅  [${p.level}][${p.type.padEnd(5)}] ${p.title}`);
    created++;
  }

  console.log(`\n🎉  Done! Created: ${created}  |  Skipped: ${skipped}`);

  // Summary table
  const counts = await prisma.readingPassage.groupBy({
    by: ['level', 'type'],
    _count: true,
    orderBy: [{ level: 'asc' }, { type: 'asc' }],
  });
  console.log('\n📊  Current DB summary:');
  for (const c of counts) {
    console.log(`     ${c.level}  ${c.type.padEnd(6)}  ${c._count} bài`);
  }
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
